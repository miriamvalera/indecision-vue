<template>
  <h2>{{ customTitle }}</h2>
  <p> {{ counter }} <sup>2</sup> = {{ squareCounter }}</p>
  <p data-testid="counter"> {{ counter }} </p>
  
  <div>
    <button v-on:click="increase">+1</button>  <!-- atajo: @click -->
    <button @click="decrease">-1</button>
  </div>
</template>

<script>
export default {
  props: {
    title: String,
    start: {
      type: Number,
      default: 100,
      //require: true
      validator( value ) {
        return value >= 0
      } 
    }
  },
    data() {
      return {
        counter: this.start
      }
    },
    methods: {
      getSquareValue() {
        return this.counter * this.counter
      },
      increase() {
        return this.counter++
      },
      decrease() {
        return this.counter--
      }
    },
    computed: {
      squareCounter() {
        return this.counter * this.counter
      },
      customTitle() {
        return this.title ? this.title : 'Counter'  // tb podriamos hacerlo as√≠:  return this.title || 'Counter'
      }
        // if (title) {
        //   return title;
        // } 
        // return "counter"

    }
}
</script>

